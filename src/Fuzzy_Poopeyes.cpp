#ifdef PRINT_EACH_DATA
			Serial << screenPage << " ";
			Serial << screenColumn << " ";
#endif //PRINT_EACH_DATA			

			//with two additions =>448us to 602us
			//with two additions + 4 ifs =>448us to 824us

